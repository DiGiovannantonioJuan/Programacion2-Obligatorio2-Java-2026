    private void cargarTabla(){
        javax.swing.table.DefaultTableModel modeloNombres = (javax.swing.table.DefaultTableModel) tablaNombresZonas.getModel();
        javax.swing.table.DefaultTableModel modeloDatos = (javax.swing.table.DefaultTableModel) tablaModelo.getModel();

        ArrayList<Zona> listaZonas = sistema.getListaZonas();

        int fila = 0;
        Iterator<Zona> iterador = listaZonas.iterator();
        while(iterador.hasNext()){
            Zona zona = iterador.next();
            modeloNombres.setValueAt(zona, fila, 0);
            int pendientes = sistema.contarPaquetesPorZonaYEstado(zona, "Pendiente");
            int enviados = sistema.contarPaquetesPorZonaYEstado(zona, "Enviado");
            int recibidos = sistema.contarPaquetesPorZonaYEstado(zona, "Recibido");
            int total = sistema.contarPaquetesPorZonaYEstado(zona, "Total");
            modeloDatos.setValueAt(pendientes, fila, 0);
            modeloDatos.setValueAt(enviados, fila, 1);
            modeloDatos.setValueAt(recibidos, fila, 2);
            modeloDatos.setValueAt(total, fila, 3);
            fila = fila + 1;
        }
    }
    

    private String obtenerEstadoPorColumna(int columna){
        String estado;
        if(columna == 0){
            estado = "Pendiente";
        }else if(columna == 1){
            estado = "Enviado";
        }else if(columna == 2){
            estado = "Recibido";
        }else{
            estado = "Total";
        }
        return estado;
    }

    private String armarMensajeDetalle(int cantidadClientes, ArrayList<Departamento> departamentos){
        String mensaje = "Cantidad de clientes distintos: " + cantidadClientes + "\n";
        mensaje = mensaje + "Departamentos de destino:\n";
        Iterator<Departamento> iterador = departamentos.iterator();
        while(iterador.hasNext()){
            Departamento departamento = iterador.next();
            mensaje = mensaje + "- " + departamento.getNombre() + "\n";
        }
        return mensaje;
    }
    
    private void mostrarInfoZona(Zona zona){
        int pendientes = sistema.contarPaquetesPorZonaYEstado(zona, "Pendiente");
        int enviados = sistema.contarPaquetesPorZonaYEstado(zona, "Enviado");
        int recibidos = sistema.contarPaquetesPorZonaYEstado(zona, "Recibido");
        boolean sinDatos = (pendientes == 0) && (enviados == 0) && (recibidos == 0);
        String mensaje;
        if (sinDatos) {
            mensaje = "Sin información";
        } else {
            mensaje = "Pendiente: " + pendientes + "\n" + "Enviado: " + enviados + "\n" + "Recibido: " + recibidos;
        }
        javax.swing.JOptionPane.showMessageDialog(this, mensaje, "Datos de la zona " + zona.getNombre(), javax.swing.JOptionPane.INFORMATION_MESSAGE);
    }
    
