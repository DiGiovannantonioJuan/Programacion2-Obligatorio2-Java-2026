    private void tablaModeloMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaModeloMouseClicked
        int fila = tablaModelo.getSelectedRow();
        int columna = tablaModelo.getSelectedColumn();
        if(fila != -1 && columna != -1){
            Zona zona = (Zona) tablaNombresZonas.getModel().getValueAt(fila, 0);
            String estado = obtenerEstadoPorColumna(columna);

            ArrayList<Paquete> paquetes = sistema.obtenerPaquetesPorZonaYEstado(zona, estado);
            int cantidadClientes = sistema.contarClientesDistintos(paquetes);
            ArrayList<Departamento> departamentos = sistema.obtenerDepartamentosDistintos(paquetes);

            String mensaje = armarMensajeDetalle(cantidadClientes, departamentos);
            javax.swing.JOptionPane.showMessageDialog(this, mensaje, "Datos de la zona " + zona.getNombre(), javax.swing.JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_tablaModeloMouseClicked

