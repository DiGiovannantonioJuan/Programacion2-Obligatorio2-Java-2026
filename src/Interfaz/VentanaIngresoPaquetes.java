    private void cargarListas(){
        ArrayList<Cliente> clientes = sistema.obtenerClientesOrdenados();
        lstListaClientes.setListData(clientes.toArray(new Cliente[0]));
        if (clientes.isEmpty()) {
            lblClientesVacia.setVisible(true);
        } else {
            lblClientesVacia.setVisible(false);
        }
        ArrayList<Departamento> departamentos = sistema.getListaDepartamentos();
        lstListaDepartamentos.setListData(departamentos.toArray(new Departamento[0]));
        if (departamentos.isEmpty()) {
            lblDepartamentosVacia.setVisible(true);
        } else {
            lblDepartamentosVacia.setVisible(false);
        }
    }
    
    @Override
    public void actualizar(){
        cargarListas();
    }    
    

