    private void btnCalcularPrecioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularPrecioActionPerformed
        String identificador = jIdentificador.getText();
        String fecha = jFecha.getText();
        String destinatario = jDestinatario.getText();
        String pesoTexto = jPesoEnGramos.getText();

        Cliente clienteSeleccionado = lstListaClientes.getSelectedValue();
        Departamento departamentoSeleccionado = lstListaDepartamentos.getSelectedValue();

        boolean camposCompletos = (clienteSeleccionado != null) && (departamentoSeleccionado != null) && (!identificador.isEmpty()) && (!fecha.isEmpty()) && (!destinatario.isEmpty()) && (!pesoTexto.isEmpty());

        if (camposCompletos) {
            boolean pesoValido = sistema.verificarCaracteresNumericos(pesoTexto);
            if (pesoValido) {
                int peso = Integer.parseInt(pesoTexto);
                precioCalculado = sistema.calcularTarifa(peso, departamentoSeleccionado);
                lblPrecioCalculado.setText("$" + precioCalculado);
                btnConfirmar.setEnabled(true);
            } else {
                JOptionPane.showMessageDialog(this,
                        "El peso debe ser numérico.",
                        "Error", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(this,
                    "Debe completar todos los campos y seleccionar un cliente y departamento.",
                    "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnCalcularPrecioActionPerformed

    private void btnConfirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConfirmarActionPerformed
        String identificador = jIdentificador.getText();
        Cliente cliente = lstListaClientes.getSelectedValue();
        String fecha = jFecha.getText();
        String destinatario = jDestinatario.getText();
        String pesoTexto = jPesoEnGramos.getText();
        Departamento departamentoSeleccionado = lstListaDepartamentos.getSelectedValue();
        if (cliente != null && departamentoSeleccionado != null) {
            int peso = Integer.parseInt(pesoTexto);
            boolean registrado = sistema.registrarPaquete(identificador, cliente, fecha, destinatario, departamentoSeleccionado, peso, precioCalculado);
            if (registrado) {
                if(VentanaIngresoPaquetesSig.getInstancia() == null){
                    VentanaIngresoPaquetesSig ventanaSig = new VentanaIngresoPaquetesSig(sistema);
                    ventanaSig.setVisible(true);
                }
                jIdentificador.setText("");
                jDestinatario.setText("");
                jPesoEnGramos.setText("");
                lblPrecioCalculado.setText("$0");
                btnConfirmar.setEnabled(false);
            } else {
                JOptionPane.showMessageDialog(this,
                        "No se pudo registrar el paquete. El identificador ya existe o es inválido.",
                        "Error", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(this,
                    "Debe seleccionar un cliente y un departamento antes de confirmar.",
                    "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnConfirmarActionPerformed

