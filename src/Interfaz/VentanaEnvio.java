    private void cargarDatosIniciales(){
        ArrayList<Funcionario> funcionarios = sistema.obtenerFuncionariosOrdenados();
        Funcionario[] arregloFuncionarios = funcionarios.toArray(new Funcionario[0]); //setListData no acepta ArrayList -> .toArray()
        lstListaFuncionarios.setListData(arregloFuncionarios);
        ArrayList<Zona> zonas = sistema.getListaZonas();
        Zona[] arregloZonas = zonas.toArray(new Zona[0]); //setListData no acepta ArrayList -> .toArray()
        lstListaZonas.setListData(arregloZonas);
        lstListaPaquetesDisponibles.setListData(new Paquete[0]);
        lstListaPaquetesAEnviar.setListData(new Paquete[0]);
        int numeroEnvio = Envio.getContadorEnvios()+1;
        jEnvioNumero.setText("Envio numero: "+numeroEnvio);
        
        if (funcionarios.isEmpty()) {
            lblFuncionariosVacia.setVisible(true);
        } else {
            lblFuncionariosVacia.setVisible(false);
        }
        if (zonas.isEmpty()) {
            lblZonasVacia.setVisible(true);
        } else {
            lblZonasVacia.setVisible(false);
        }
        actualizarTotales();
    }
    
    private void actualizarTotales(){
        int cantidad = paquetesAEnviar.size();
        int pesoTotalEnGramos = sistema.calcularPesoTotal(paquetesAEnviar);
        int montoTotal = sistema.calcularMontoTotal(paquetesAEnviar);
        int kg = pesoTotalEnGramos / 1000;
        int gramos = pesoTotalEnGramos % 1000;

        jCantidadPaquetes.setText("Cantidad de paquetes: " + cantidad);
        jPesoTotal.setText("Peso: " + kg + " kg, " + gramos + " gramos");
        jMontoTotal.setText("Monto: $" + montoTotal);
    }
    
    @Override
    public void actualizar(){
        cargarDatosIniciales();
        actualizarTotales();
    }
