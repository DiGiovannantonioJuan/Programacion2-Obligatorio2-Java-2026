    private void lstListaZonasValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_lstListaZonasValueChanged
        Zona zonaSeleccionada = lstListaZonas.getSelectedValue();
        boolean cambioDeZona = zonaSeleccionada != null && zonaSeleccionada != zonaActual;
        boolean cambioDeZonaInvalido = cambioDeZona && !paquetesAEnviar.isEmpty();
        boolean cambioDeZonaValido = cambioDeZona && paquetesAEnviar.isEmpty();
        if (cambioDeZonaInvalido) {
            javax.swing.JOptionPane.showMessageDialog(this, "Ya hay paquetes seleccionados de la zona " + zonaActual + ". Quite todos los paquetes antes de cambiar de zona.");
            lstListaZonas.setSelectedValue(zonaActual, true);
        } else if (cambioDeZonaValido) {
            zonaActual = zonaSeleccionada;
            paquetesDisponibles = sistema.obtenerPaquetesPendientesPorZona(zonaSeleccionada);
            Paquete[] arregloPendientes = paquetesDisponibles.toArray(new Paquete[0]); //setListData no acepta ArrayList -> .toArray(). Ademas hay que hacer cast explicito (new Paquete)
            lstListaPaquetesDisponibles.setListData(arregloPendientes);
        }
    }//GEN-LAST:event_lstListaZonasValueChanged

    private void btnAgregarPaqueteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarPaqueteActionPerformed
        Paquete paqueteSeleccionado = lstListaPaquetesDisponibles.getSelectedValue();
        if(paqueteSeleccionado !=null){
            paquetesAEnviar.add(paqueteSeleccionado);
            paquetesDisponibles.remove(paqueteSeleccionado);
            Paquete[] arregloDisponibles = paquetesDisponibles.toArray(new Paquete[0]);
            lstListaPaquetesDisponibles.setListData(arregloDisponibles);
            Paquete[] arregloAEnviar = paquetesAEnviar.toArray(new Paquete[0]);
            lstListaPaquetesAEnviar.setListData(arregloAEnviar);
            actualizarTotales();
        }
    }//GEN-LAST:event_btnAgregarPaqueteActionPerformed

    private void btnQuitarPaqueteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnQuitarPaqueteActionPerformed
        Paquete paqueteSeleccionado = lstListaPaquetesAEnviar.getSelectedValue();
        if (paqueteSeleccionado != null) {
            paquetesAEnviar.remove(paqueteSeleccionado);
            paquetesDisponibles.add(paqueteSeleccionado);
            Paquete[] arregloAEnviar = paquetesAEnviar.toArray(new Paquete[0]);
            lstListaPaquetesAEnviar.setListData(arregloAEnviar);
            Paquete[] arregloDisponibles = paquetesDisponibles.toArray(new Paquete[0]);
            lstListaPaquetesDisponibles.setListData(arregloDisponibles);
            if (paquetesAEnviar.isEmpty()) {
                zonaActual = null;
            }
            actualizarTotales();
        }
    }//GEN-LAST:event_btnQuitarPaqueteActionPerformed

