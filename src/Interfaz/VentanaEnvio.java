    private void btnConfirmarEnvioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConfirmarEnvioActionPerformed
        Funcionario funcionarioSeleccionado = lstListaFuncionarios.getSelectedValue();
        String fecha = jFechaEnvio.getText();
        Zona zonaSeleccionada = lstListaZonas.getSelectedValue();

        if (funcionarioSeleccionado == null) {
            javax.swing.JOptionPane.showMessageDialog(this, "Debe seleccionar un funcionario");
        } else if (fecha.isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "Debe ingresar la fecha");
        } else if (zonaSeleccionada == null) {
            javax.swing.JOptionPane.showMessageDialog(this, "Debe seleccionar una zona");
        } else if (paquetesAEnviar.isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "Debe seleccionar al menos un paquete");
        } else {
            sistema.registrarEnvio(fecha, zonaSeleccionada, funcionarioSeleccionado, paquetesAEnviar);
            javax.swing.JOptionPane.showMessageDialog(this, "Envío registrado con éxito");
            this.dispose();
        }
    }//GEN-LAST:event_btnConfirmarEnvioActionPerformed

