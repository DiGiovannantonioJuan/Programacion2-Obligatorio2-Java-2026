    private void btnEditarClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarClienteActionPerformed
        Cliente clienteSeleccionado = lstlistaClientes.getSelectedValue();
        if (clienteSeleccionado == null) {
            javax.swing.JOptionPane.showMessageDialog(this, "Debe seleccionar un cliente de la lista",
                    "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
        } else {
            boolean modificado = sistema.modificarCliente(clienteSeleccionado.getNombre(),
                    jNombre.getText(), jCelular.getText(), jCorreo.getText());
            if(modificado){
                javax.swing.JOptionPane.showMessageDialog(this, "Cliente modificado correctamente");
                cargarListaClientes();
            }else{
                javax.swing.JOptionPane.showMessageDialog(this, "No se pudo modificar el cliente. "
                        + "Verifique que el nombre y el celular sean válidos y únicos", "Error",
                        javax.swing.JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnEditarClienteActionPerformed

    private void lstlistaClientesValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_lstlistaClientesValueChanged
        Cliente clienteSeleccionado = lstlistaClientes.getSelectedValue();
        if(clienteSeleccionado != null){
            jNombre.setText(clienteSeleccionado.getNombre());
            jCelular.setText(clienteSeleccionado.getCelular());
            jCorreo.setText(clienteSeleccionado.getCorreoElectronico());
        }
    }//GEN-LAST:event_lstlistaClientesValueChanged
    
    
