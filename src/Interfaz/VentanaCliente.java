    public void cargarListaClientes(){
        ArrayList<Cliente> clientes = sistema.obtenerClientesOrdenados();
        lstlistaClientes.setListData(clientes.toArray(new Cliente[0]));
        if(clientes.isEmpty()){
            lblClientesVacia.setVisible(true);
            jNombre.setVisible(false);
            jCelular.setVisible(false);
            jCorreo.setVisible(false);
        }else{
            lblClientesVacia.setVisible(false);
            jNombre.setVisible(true);
            jCelular.setVisible(true);
            jCorreo.setVisible(true);
        }
    }
    
