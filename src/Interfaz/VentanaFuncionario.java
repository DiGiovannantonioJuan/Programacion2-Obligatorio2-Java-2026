    public void cargarListaFuncionarios(){
        ArrayList<Funcionario> listaOrdenada = sistema.obtenerFuncionariosOrdenados();
        lstListaFuncionarios.setListData(listaOrdenada.toArray(new Funcionario[0]));
        if (listaOrdenada.isEmpty()) {
            lblFuncionariosVacia.setVisible(true);
            jNombre.setVisible(false);
            jCelular.setVisible(false);
            jNumeroFuncionario.setVisible(false);
            jIngreso.setVisible(false);
            jCargo.setVisible(false);
        } else {
            lblFuncionariosVacia.setVisible(false);
            jNombre.setVisible(true);
            jCelular.setVisible(true);
            jNumeroFuncionario.setVisible(true);
            jIngreso.setVisible(true);
            jCargo.setVisible(true);
        }
    }
    
