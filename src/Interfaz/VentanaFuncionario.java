    private void jEditarFuncionarioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jEditarFuncionarioActionPerformed
        Funcionario funcionarioSeleccionado = lstListaFuncionarios.getSelectedValue();
        if(funcionarioSeleccionado == null){
            javax.swing.JOptionPane.showMessageDialog(this, "Debe seleccionar un funcionario de la lista",
                    "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
        }else{
            String numeroTexto = jNumeroFuncionario.getText();
            String añoTexto = jIngreso.getText();
            boolean datosNumericosValidos = sistema.verificarCaracteresNumericos(numeroTexto) && sistema.verificarCaracteresNumericos(añoTexto);
            if(datosNumericosValidos){
                int numero = Integer.parseInt(numeroTexto);
                int año = Integer.parseInt(añoTexto);
                boolean modificado = sistema.modificarFuncionario(funcionarioSeleccionado.getNombre(),
                        jNombre.getText(), jCelular.getText(), numero, año);
                if(modificado){
                    javax.swing.JOptionPane.showMessageDialog(this, "Funcionario modificado correctamente");
                    cargarListaFuncionarios();
                }else{
                    javax.swing.JOptionPane.showMessageDialog(this, "No se pudo modificar el funcionario. "
                            + "Verifique que el nombre sea valido y unico", "Error",
                            javax.swing.JOptionPane.ERROR_MESSAGE);
                }
            }else{
                javax.swing.JOptionPane.showMessageDialog(this,
                        "El número de funcionario y el año de ingreso deben ser numéricos.",
                        "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_jEditarFuncionarioActionPerformed

    private void lstListaFuncionariosValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_lstListaFuncionariosValueChanged
        Funcionario funcionarioSeleccionado = lstListaFuncionarios.getSelectedValue();
        if(funcionarioSeleccionado != null){
            jNombre.setText(funcionarioSeleccionado.getNombre());
            jCelular.setText(funcionarioSeleccionado.getCelular());
            jNumeroFuncionario.setText(String.valueOf(funcionarioSeleccionado.getNumeroFuncionario()));
            jIngreso.setText(String.valueOf(funcionarioSeleccionado.getAñoIngreso()));
        }
    }//GEN-LAST:event_lstListaFuncionariosValueChanged

    private void jFiltrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jFiltrarActionPerformed
        String cargoFiltro = jFiltroCargo.getText().trim();
        if (cargoFiltro.isEmpty()) {
            cargarListaFuncionarios();
        } else {
            ArrayList<Funcionario> filtrados = sistema.buscarFuncionariosPorCargo(cargoFiltro);
            lstListaFuncionarios.setListData(filtrados.toArray(new Funcionario[0]));
        } 
    }//GEN-LAST:event_jFiltrarActionPerformed

