    private void btnPorcentajeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPorcentajeActionPerformed
        // TODO add your handling code here:
        String textoPorcentaje = jPorcentaje.getText();
        boolean textoValido = !textoPorcentaje.isEmpty();
        if (textoValido) {
            try {
                double porcentaje = Double.parseDouble(textoPorcentaje); //el porcentaje puede ser decimal, positivo (aumento) o negativo (descuento)
                sistema.actualizarTarifas(porcentaje);
                cargarListaTarifas();
                JOptionPane.showMessageDialog(this, "Tarifas actualizadas correctamente.");
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(this,
                        "El porcentaje debe ser un número válido.",
                        "Error", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(this,
                    "Debe ingresar un porcentaje.",
                    "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnPorcentajeActionPerformed

