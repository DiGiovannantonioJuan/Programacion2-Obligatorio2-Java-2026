    private void cargarListaTarifas(){
        ArrayList<Tarifa> tarifas = sistema.getListaTarifas();
        lstListaTarifas.setListData(tarifas.toArray(new Tarifa[0]));
        if (tarifas.isEmpty()) {
            lblTarifasVacia.setVisible(true);
        } else {
            lblTarifasVacia.setVisible(false);
        }
    }

