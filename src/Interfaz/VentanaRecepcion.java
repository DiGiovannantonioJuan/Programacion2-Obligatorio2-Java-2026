    private void cargarDatosIniciales(){
        ArrayList<Envio> envios = sistema.obtenerEnviosOrdenados();
        Envio[] arregloEnvios = envios.toArray(new Envio[0]);
        lstListaEnvios.setListData(arregloEnvios);
        lstListaEnvios.setCellRenderer(new RenderizadorEnvios());
        if (envios.isEmpty()) {
            lblEnviosVacia.setVisible(true);
        } else {
            lblEnviosVacia.setVisible(false);
        }
    }
    
