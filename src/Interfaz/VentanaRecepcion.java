    private class RenderizadorEnvios extends DefaultListCellRenderer {
        @Override
        public Component getListCellRendererComponent(JList<?> lista, Object valor, int indice, boolean seleccionado, boolean tieneFoco) {
            Component componente = super.getListCellRendererComponent(lista, valor, indice, seleccionado, tieneFoco);
            Envio envio = (Envio) valor;
            Color colorBase;
            if (envio.isRecepcionado()) {
                colorBase = Color.GREEN;
            } else {
                colorBase = Color.YELLOW;
            }
            if (seleccionado) {
                componente.setBackground(colorBase.darker());
            } else {
                componente.setBackground(colorBase);
            }
            return componente;
        }
    }
    @Override
    public void actualizar(){
        cargarDatosIniciales();
    }
