    private void lstListaEnviosValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_lstListaEnviosValueChanged
        Envio envioSeleccionado = lstListaEnvios.getSelectedValue();
        if (envioSeleccionado != null) {
            ArrayList<Paquete> paquetes = envioSeleccionado.getListaPaquetes();
            Paquete[] arregloPaquetes = paquetes.toArray(new Paquete[0]);
            lstListaResultante.setListData(arregloPaquetes);
            if (envioSeleccionado.isRecepcionado()) {
                lstListaResultante.setEnabled(false);
            } else {
                lstListaResultante.setEnabled(true);
                lstListaResultante.setSelectionMode(javax.swing.ListSelectionModel.MULTIPLE_INTERVAL_SELECTION);
            }
        }
    }//GEN-LAST:event_lstListaEnviosValueChanged

    private void btnConfirmarRecepcionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConfirmarRecepcionActionPerformed
        Envio envioSeleccionado = lstListaEnvios.getSelectedValue();
        if (envioSeleccionado != null) {
            if (!envioSeleccionado.isRecepcionado()) {
                ArrayList<Paquete> paquetesEntregados = new ArrayList<Paquete>(lstListaResultante.getSelectedValuesList());
                if (paquetesEntregados.isEmpty()) {
                    javax.swing.JOptionPane.showMessageDialog(this, "Debe seleccionar al menos un paquete entregado.");
                } else {
                    sistema.confirmarRecepcion(envioSeleccionado, paquetesEntregados);
                    sistema.guardarSistema();
                    cargarDatosIniciales();
                    Paquete[] listaVacia = new Paquete[0];
                    lstListaResultante.setListData(listaVacia);
                }
            }
        }
    }//GEN-LAST:event_btnConfirmarRecepcionActionPerformed

