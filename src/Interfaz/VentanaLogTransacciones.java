    public void cargarLog(){
        java.util.ArrayList<String> lineas = sistema.obtenerLogTransacciones();
        lstLogTransacciones.setListData(lineas.toArray(new String[0]));
        if (lineas.isEmpty()) {
            lblLogVacio.setVisible(true);
        } else {
            lblLogVacio.setVisible(false);
        }
    }
    
