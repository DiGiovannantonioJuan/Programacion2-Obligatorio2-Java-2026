    private void btnBorrarLogActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBorrarLogActionPerformed
        int opcionElegida = JOptionPane.showConfirmDialog(this,
                "¿Está seguro que desea borrar todo el contenido del log de transacciones?",
                "Confirmar borrado",
                JOptionPane.YES_NO_OPTION);
        boolean confirmado = (opcionElegida == JOptionPane.YES_OPTION);
        if(confirmado){
            sistema.borrarLogTransacciones();
            cargarLog();
        }
    }//GEN-LAST:event_btnBorrarLogActionPerformed

